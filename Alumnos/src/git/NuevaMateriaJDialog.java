    private void jBaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBaceptarActionPerformed

        try {
            Integer semestre = Integer.parseInt(jTsemestre.getText());
            Integer creditos = Integer.parseInt(jTcreditos.getText());
            Materia materia = new Materia(jTclave.getText(), jTnombremateria.getText(), semestre, creditos, jTprofesor.getText(), (Status) jCstatus.getSelectedItem());

            jLIclave.setText(materia.getClave());
            jLInombre.setText(materia.getNombre());
            jLIsemestre.setText(semestre.toString());
            jLIcreditos.setText(creditos.toString());
            jLIprofesor.setText(materia.getProfesor());
            jLIstatus.setText(materia.getStatus().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this,
                "¬°¬°¬°Rellane todos los campos!!!",
                "Error al ingresar datos",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBaceptarActionPerformed

